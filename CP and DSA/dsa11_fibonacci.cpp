int fib(int n)
{
    int t0 = 0, t1 = 1, s = 0;
    for (int i = 0; i <= 1;i++)
    {
        if (i <= 1)
            cout << i <<", ";
    }
    for (int i = 2; i <= n; i++)
    {
        s = t0 + t1;
        t0 = t1;
        t1 = s;
        cout << s ;
        if (i<n)
        {
            cout << ", ";
        }
        
    }
    cout << " ;" ;
}

